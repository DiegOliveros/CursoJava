    private void saveBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBTNActionPerformed
        usuario.setId(idTF.getText());
        usuario.setNombre(nameTF.getText());
        usuario.setApellido(lastnTF.getText());
        usuario.setEdad(edadS.getValue().toString());
        if (manBTN.isSelected()) {
            usuario.setGenero("Hombre");
        } else {
            usuario.setGenero("Mujer");
        }
        usuario.setEmail(mailTF.getText());
        usuario.setRh(rhList.getSelectedItem().toString());
        usuario.setPath(ruta);
        desactivarEdicion();
    }//GEN-LAST:event_saveBTNActionPerformed

    private void cancelBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelBTNActionPerformed
        desactivarEdicion();
        aplicarUsuario(usuario);
    }//GEN-LAST:event_cancelBTNActionPerformed

