    public void aplicarUsuario(Usuario usuario) {
        this.usuario = usuario;
        idTF.setText(usuario.getId());
        nameTF.setText(usuario.getNombre());
        lastnTF.setText(usuario.getApellido());
        edadS.getModel().setValue(Integer.parseInt(usuario.getEdad()));
        if (usuario.getGenero().equals("Hombre")) {
            manBTN.setSelected(true);
        } else {
            womanBTN.setSelected(true);
        }
        mailTF.setText(usuario.getEmail());
        rhList.setSelectedItem(usuario.getRh());
        imageLabel.setIcon(icono(usuario.getPath()));
    }

