    private void contactosTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_contactosTableMouseClicked
        if (contactosTable.getSelectedRow() != -1) {
            Contacto contacto = buscarContacto(contactosTable.getValueAt(contactosTable.getSelectedRow(), 0).toString());
            nameLbl.setText(contacto.getNombre());
            phoneLbl.setText(contacto.getTelefono() + "");
            ageLbl.setText(contacto.getEdad() + "");
            genderLbl.setText(contacto.getSexo());
            mailLbl.setText(contacto.getEmail());
        } else {
            nameLbl.setText("");
            phoneLbl.setText("");
            ageLbl.setText("");
            genderLbl.setText("");
            mailLbl.setText("");
        }
    }//GEN-LAST:event_contactosTableMouseClicked

    public void resetList() {
        for (int i = contactosTable.getRowCount() - 1; i >= 0; i--) {
            modelo.removeRow(i);
        }
    }

    public void llenarTabla() {
        resetList();
        Contacto[] contactos = agenda.getContactos();
        Object[] row;
        for (Contacto contacto : contactos) {
            if (contacto != null) {
                row = new Object[2];
                row[0] = contacto.getNombre();
                row[1] = contacto.getTelefono();
                modelo.addRow(row);
            }
        }
    }

    public Contacto buscarContacto(String c) {
        Contacto[] contactos = agenda.getContactos();
        for (Contacto contacto : contactos) {
            if (contacto.getNombre().equalsIgnoreCase(c)) {
                return contacto;
            }
        }
        return null;
    }

