    private void eraseBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eraseBTNActionPerformed
        if (contactosTable.getSelectedRow() != -1) {
            int option = JOptionPane.showConfirmDialog(this, "Seguro que desea eliminar este contacto?");
            if (option == 0) {
                Contacto contacto = buscarContacto(contactosTable.getValueAt(contactosTable.getSelectedRow(), 0).toString());
                agenda.eliminarContacto(contacto);
                modelo.removeRow(contactosTable.getSelectedRow());
                llenarTabla();
            }
        } else {
        }
        
    }//GEN-LAST:event_eraseBTNActionPerformed

    private void editBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBTNActionPerformed
        if (contactosTable.getSelectedRow() != -1) {
            EditarUI editar = new EditarUI();
            editar.parentar(this);
            this.setEnabled(false);
            editar.setVisible(true);
            editar.editar(buscarContacto(contactosTable.getValueAt(contactosTable.getSelectedRow(), 0).toString()));
        }
    }//GEN-LAST:event_editBTNActionPerformed

